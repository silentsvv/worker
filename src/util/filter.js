function filterOn2(data, size) {
    if (!Array.isArray(data)) return false;

    const filter = [];
    for (let i = 0, len = data.length; i < len; i++) {
        for (let j = 0, jlen = data.length; j < jlen; j++) {
            ;
        }
    }

    return filter
}

function filterOnlogn(n, size) {
    for (let i = 0; i < n; i++) {
        for (let j = 1; j < n; j = j * 2) {
            ;
        }
    }

    return size
}

